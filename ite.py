# Menggunakan iter() dan next()
fruits = ["apple", "banana", "cherry"]
fruit_iterator = iter(fruits)
print(next(fruit_iterator))  # Output: apple
print(next(fruit_iterator))  # Output: banana
print(next(fruit_iterator))  # Output: cherry



